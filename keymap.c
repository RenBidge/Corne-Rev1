bool process_record_user(uint16_t keycode, keyrecord_t *record) {
    anim_sleep = timer_read32();
    oled_on();
    switch (keycode) {
        /* KEYBOARD PET STATUS START */
        case KC_RSFT:
        case KC_LSFT:
            if (record->event.pressed) {
                isBarking = true;
            } else {
                isBarking = false;
            }
            break;
        case KC_LCTL:
        case KC_RCTL:
            if (record->event.pressed) {
                isSneaking = true;
            } else {
                isSneaking = false;
            }
            break;
        case KC_SPC:
            if (record->event.pressed) {
                isJumping  = true;
                showedJump = false;
            } else {
                isJumping = false;
            }
            break;
        /* KEYBOARD PET STATUS END */
    }
    return true;
}