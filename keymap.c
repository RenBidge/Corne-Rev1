static void render_bongo(void) {
    void animation_phase(void) {
        if (current_wpm <= IDLE_SPEED) {
            oled_write_raw_P(idle, ANIM_SIZE_BONGO);
        }
        if (current_wpm > IDLE_SPEED && current_wpm < TAP_SPEED) {
            oled_write_raw_P(prep, ANIM_SIZE_BONGO);
        }
        if (current_wpm >= TAP_SPEED) {
            current_tap_frame = (current_tap_frame + 1) % TAP_FRAMES;
            oled_write_raw_P(tap[abs((TAP_FRAMES - 1) - current_tap_frame)], ANIM_SIZE_BONGO);
        }
    }

    if (timer_elapsed32(anim_timer_bongo) > ANIM_FRAME_DURATION) {
        anim_timer_bongo = timer_read32();
        animation_phase();
    }
}

/* logic */
static void render_luna(int LUNA_X, int LUNA_Y) {
    /* animation */
    void animate_luna(void) {
        /* jump */
        if (isJumping || !showedJump) {
            /* clear */
            oled_set_cursor(LUNA_X, LUNA_Y + 2);
            oled_write("     ", false);

            oled_set_cursor(LUNA_X, LUNA_Y - 1);

            showedJump = true;
        } else {
            /* clear */
            oled_set_cursor(LUNA_X, LUNA_Y - 1);
            oled_write("     ", false);

            oled_set_cursor(LUNA_X, LUNA_Y);
        }

        /* switch frame */
        current_frame = (current_frame + 1) % 2;

        /* current status */
        if (isBarking) {
            oled_write_raw_P(bark[current_frame], ANIM_SIZE);
        } else if (isSneaking) {
            oled_write_raw_P(sneak[current_frame], ANIM_SIZE);
        } else if (current_wpm <= MIN_WALK_SPEED) {
            oled_write_raw_P(sit[current_frame], ANIM_SIZE);
        } else if (current_wpm <= MIN_RUN_SPEED) {
            oled_write_raw_P(walk[current_frame], ANIM_SIZE);
        } else {
            oled_write_raw_P(run[current_frame], ANIM_SIZE);
        }
    }

    /* animation timer */
    if (current_wpm != 000) {
        if (timer_elapsed32(anim_timer_luna) > ANIM_FRAME_DURATION) {
            anim_timer_luna = timer_read32();
            animate_luna();
        }
    } else {
        if (timer_elapsed32(anim_sleep) > OLED_TIMEOUT) {
            oled_off();
        } else {
            if (timer_elapsed32(anim_timer_luna) > ANIM_FRAME_DURATION) {
                anim_timer_luna = timer_read32();
                animate_luna();
            }
        }
    }
    
}

